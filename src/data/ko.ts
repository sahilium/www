export function getCurrentKo(): Ko {
  return findKo(new Date());
}

export function getKoForDate(date: Date): Ko {
  return findKo(date);
}

function findKo(date: Date): Ko {
  const month = date.getMonth() + 1;
  const day = date.getDate();
  const todayDOY = dayOfYear(month, day);

  for (let i = 0; i < allKo.length; i++) {
    const startDOY = dayOfYear(allKo[i].startMonth, allKo[i].startDay);
    const next = allKo[i + 1];
    const endDOY = next
      ? dayOfYear(next.startMonth, next.startDay) - 1
      : 365;

    if (todayDOY >= startDOY && todayDOY <= endDOY) {
      return allKo[i];
    }
  }

  return allKo[allKo.length - 1];
}

export function getPreviousKo(ko: Ko): Ko {
  const idx = allKo.indexOf(ko);
  if (idx === -1) return allKo[allKo.length - 1];
  return allKo[(idx - 1 + allKo.length) % allKo.length];
}

export function getNextKo(ko: Ko): Ko {
  const idx = allKo.indexOf(ko);
  if (idx === -1) return allKo[0];
  return allKo[(idx + 1) % allKo.length];
}
